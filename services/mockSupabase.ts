export const getTeamById = async (id: string): Promise<Team | undefined> => {
  const { data, error } = await supabase
    .from('teams')
    .select('*, members(*), evaluations(*)')
    .eq('id', id)
    .single();

  if (error || !data) {
    return undefined;
  }

  return mapSqlToTeam(data);
};

export const getTeamByUserEmail = async (email: string): Promise<Team | undefined> => {
  // First check if user is a lead
  const { data: leadData } = await supabase
    .from('teams')
    .select('*, members(*), evaluations(*)')
    .eq('lead_email', email)
    .single();

  if (leadData) return mapSqlToTeam(leadData);

  // Then check if user is a member
  // Note: This requires a 2-step lookup or a join if we want to be efficient, 
  // but for now 2-step is safer with the current schema capabilities.
  const { data: memberData } = await supabase
    .from('members')
    .select('team_id')
    .eq('email', email)
    .single();

  if (memberData && memberData.team_id) {
    return getTeamById(memberData.team_id);
  }

  return undefined;
};

export const createTeam = async (teamData: Omit<Team, 'id' | 'status' | 'evaluations' | 'totalScore' | 'submissionDate' | 'panel'>, file: File | null): Promise<Team> => {

  // 1. Generate ID
  const generateUniqueId = () => {
    const chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789';
    let result = 'SUB-';
    for (let i = 0; i < 6; i++) {
      result += chars.charAt(Math.floor(Math.random() * chars.length));
    }
    return result;
  };
  const submissionId = generateUniqueId();

  // 2. Determine PPT URL (File Upload is Mandatory, but we preserve Manual Link if given)
  let finalPptUrl = '';
  const manualLink = teamData.pptUrl;

  // If a file is uploaded, it becomes the primary PPT URL
  if (file) {
    const fileExt = file.name.split('.').pop();
    const fileName = `${submissionId}.${fileExt}`;
    const filePath = `${submissionId}/${fileName}`;

    // Try uploading to 'documents' bucket
    const bucketName = 'documents';
    const { error: uploadError } = await supabase.storage
      .from(bucketName)
      .upload(filePath, file);

    if (uploadError) {
      if (uploadError.message.includes("Bucket not found")) {
        console.error("CRITICAL: The 'documents' bucket does not exist in your Supabase Storage. Please create a public bucket named 'documents'.");
      }
      console.warn("File upload failed:", uploadError.message);
    } else {
      const { data } = supabase.storage.from(bucketName).getPublicUrl(filePath);
      finalPptUrl = data.publicUrl;
    }
  }

  // 3. Prepare Payload
  // If user provided a Manual Link, we append it to the description so it's not lost.
  let finalDesc = teamData.ideaDesc;
  if (manualLink && manualLink.trim() !== '') {
    finalDesc += `\n\n[Additional Link]: ${manualLink}`;
  }

  // 3. Insert Team
  const teamPayload = {
    id: submissionId,
    team_name: teamData.teamName,
    lead_name: teamData.leadName,
    lead_email: teamData.email,
    lead_phone: teamData.phone,
    team_size: teamData.teamSize,
    ps_id: teamData.psId,
    idea_desc: finalDesc,
    ppt_url: finalPptUrl, // Stores the uploaded file link
    status: TeamStatus.SUBMITTED,
    total_score: 0
  };

  const { error: insertError } = await supabase.from('teams').insert(teamPayload);
  if (insertError) throw new Error(insertError.message);

  // 4. Insert Members
  if (teamData.members.length > 0) {
    const membersPayload = teamData.members.map(m => ({
      team_id: submissionId,
      name: m.name,
      email: m.email,
      phone: m.phone
    }));
    const { error: memberError } = await supabase.from('members').insert(membersPayload);
    if (memberError) console.error("Error inserting members:", memberError);
  }

  // Return constructed team object for UI
  return {
    ...teamData,
    id: submissionId,
    status: TeamStatus.SUBMITTED,
    evaluations: {},
    totalScore: 0,
    submissionDate: new Date().toISOString(),
    panel: undefined,
    adminPptScore: undefined,
    pptUrl: finalPptUrl
  };
};

