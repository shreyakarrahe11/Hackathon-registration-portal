// Helper to calculate total score (re-used logic but fetched fresh)
const calculateTotalScore = (adminScore: number, evaluations: any[]): number => {
  let avgJudgeScore = 0;
  if (evaluations.length > 0) {
    const sumJudgeScores = evaluations.reduce((acc, curr) => acc + curr.score, 0);
    avgJudgeScore = sumJudgeScores / evaluations.length;
  }
  return (adminScore || 0) + avgJudgeScore;
};

