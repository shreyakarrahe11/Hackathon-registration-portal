const mapSqlToTeam = (data: any): Team => {
  const evaluations: Record<string, Evaluation> = {};

  if (data.evaluations && Array.isArray(data.evaluations)) {
    data.evaluations.forEach((ev: any) => {
      evaluations[ev.judge_name] = { presentationScore: ev.score };
    });
  }

  return {
    id: data.id,
    teamName: data.team_name,
    leadName: data.lead_name,
    email: data.lead_email,
    phone: data.lead_phone,
    teamSize: data.team_size,
    members: data.members || [],
    psId: data.ps_id,
    ideaDesc: data.idea_desc,
    pptUrl: data.ppt_url || '#',
    panel: data.panel,
    adminPptScore: data.admin_ppt_score,
    status: data.status as TeamStatus,
    evaluations: evaluations,
    totalScore: data.total_score || 0,
    submissionDate: data.submission_date
  };
};

